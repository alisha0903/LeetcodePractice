class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        // code here
        Node *temp,*end;
        temp=end=head;
        vector<pair<int,int>>res;
        while(end->next)
        end=end->next;
        while(end->data>temp->data)
        {
            if(temp->data+end->data==target)
            {
            res.push_back({temp->data,end->data});
            temp=temp->next;
            end=end->prev;
            }
            else if(temp->data+end->data>target)
            end=end->prev;
            else 
            temp=temp->next;
            
        }
        return res;
        
        
    }
};

